package Variable;

public class vari {
    int a = 10; // Integer variable
    double b = 20.5; // Double variable     
    char c = 'A'; // Character variable
    String d = "Hello"; // String variable
    boolean e = true; // Boolean variable

    public static void main(String[] args) {
        vari var = new vari();
        System.out.println("Integer: " + var.a);
        System.out.println("Double: " + var.b);
        System.out.println("Character: " + var.c);
        System.out.println("String: " + var.d);
        System.out.println("Boolean: " + var.e);
    }
    
}
