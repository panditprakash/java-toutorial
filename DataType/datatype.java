package DataType;

public class datatype {
    int a = 10; // Integer variable
    double b = 20.5; // Double variable     
    char c = 'A'; // Character variable
    String d = "Hello"; // String variable
    boolean e = true; // Boolean variable

    public static void main(String[] args) {
        datatype dt = new datatype();
        System.out.println("Integer: " + dt.a);
        System.out.println("Double: " + dt.b);
        System.out.println("Character: " + dt.c);
        System.out.println("String: " + dt.d);
        System.out.println("Boolean: " + dt.e);
    }
    
}
